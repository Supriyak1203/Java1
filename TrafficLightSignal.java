
class TrafficLightSignal
{
public static void main(String[]args)
{
char colour='R';
if(colour=='R')
{
System.out.println("They must be Stop!");
}
else if(colour=='Y')
{
System.out.println("They must Slow Down!");
}
else if(colour=='G')
{
System.out.println("They can Go ahead! ");
}
else
{
System.out.println("Invalid colour");
} 
}
}

